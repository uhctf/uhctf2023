
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

struct node
{
	char c;
	struct node *n;
};

struct node *loadMessage(char *message)
{
	struct node *head = NULL;
	struct node *p = head;
	for (int i = 0; i < strlen(message); i++)
	{
		struct node *t = (struct node *)malloc(sizeof(struct node));
		t->c = message[i];
		t->n = NULL;

		if (p == NULL)
		{
			head = t;
			p = t;
		}
		else
		{
			p->n = t;
			p = t;
		}
	}
	return head;
}

void printFlag(struct node *flag, FILE *fp)
{
	struct node *p = flag;
	while (p != NULL)
	{
		fprintf(fp, "%c", p->c);
		p = p->n;
	}
	fprintf(fp, "\n");
	return;
}

void swap(struct node **h, int x, int y)
{
	if (x == y)
	{
		return;
	}

	int xi = 0;
	struct node *px = NULL, *cx = *h;
	while (cx && xi != x)
	{
		px = cx;
		cx = cx->n;
		xi++;
	}

	int yi = 0;
	struct node *py = NULL, *cy = *h;
	while (cy && yi != y)
	{
		py = cy;
		cy = cy->n;
		yi++;
	}

	if (cx == NULL || cy == NULL)
	{
		return;
	}

	if (px != NULL)
	{
		px->n = cy;
	}
	if (py != NULL)
	{
		py->n = cx;
	}
	if (px == NULL)
	{
		*h = cy;
	}
	if (py == NULL)
	{
		*h = cx;
	}

	cx->n = cy->n;
	cy->n = cx->n;

	return;
}
